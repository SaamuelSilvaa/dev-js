 // operadores de comparação

 // igualdade
 console.log(5 == '5')

 // igualdade estrita
 console.log(5 === '5')

 //diferente 
 console.log(5 != '5')

 // diferente estrito
 console.log(5 !== '5')

 // maior
 console.log(10 > 5 )

 //maior ou igual
 console.log(10 >= 5)

 // menor
 console.log(10 < 5)

 //menor ou igual
console.log(10 <= 5)

